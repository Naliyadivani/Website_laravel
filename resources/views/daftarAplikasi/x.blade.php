                                informasi = `<div class="mr-1 flex-shrink-0 text-right">
                                                    <i class="fas fa-info-circle text-primary" data-toggle="popover" data-trigger="click" data-html="true" data-content="${text}"></i>
                                                </div>`
                                trip_status = `<div class="d-flex align-items-center justify-content-center p-0 rounded">
                                                    ${informasi}
                                                    <span class="label label-primary label-inline font-weight-bolder" style="color: #E03046; background-color:#FFD6D6;">Rejected</span>
                                                </div>`
                                // trip_status = `<span class="label label-primary label-inline font-weight-bolder" style="color: #E03046; background-color:#FFD6D6;">Rejected</span>`
                            }
                            if (x.status == 'WaitApv') {
                                trip_status = `<span class="label label-primary label-inline font-weight-bolder" style="color:#D8891D; background-color:#FFE7C7;">Waiting Approval</span>`
                            }
                            if (x.status == 'Drafted') {
                                trip_status = `<span class="label label-danger label-inline font-weight-bolder" style="color:#F64E60; background-color:#FFE2E5;">Drafted</span>`
                            }
                            table.row.add([
                                i + 1,
                                x.tanggal_kegiatan,
                                x.nama_kegiatan,
                                x.lokasi_kegiatan,
                                trip_status,
                                action
                            ]).draw(false)
                        });
                    }
                    KTApp.unblock('#kt_datatable')
                },
                error: function(data) {
                    KTApp.unblock('#kt_datatable')
                    handleUnauthorized(data);
                }
            })
        }


