    function sortListExpenseType(listExpenseType) {
        return listExpenseType.sort((a, b) => {
                    let fa = a.currency.toLowerCase(),
                        fb = b.currency.toLowerCase();

                    if (fa < fb) {
                        return -1;
                    }
                    if (fa > fb) {
                        return 1;
                    }
                    return 0;
                })
    }

